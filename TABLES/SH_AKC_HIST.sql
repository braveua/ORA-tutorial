CREATE TABLE "CREATOR"."SH_AKC_HIST" (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE NOT NULL ENABLE,
    EVENT_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    PROD_ID NUMBER NOT NULL,
    OPERATION VARCHAR2(10) NOT NULL,
    DB_USER VARCHAR2(128) DEFAULT SYS_CONTEXT('USERENV', 'SESSION_USER') NOT NULL,
    -- Новые значения
    NEW_NAME VARCHAR2(100)
);

COMMENT ON TABLE "CREATOR"."SH_AKC_HIST" IS 'История изменений таблицы SH_AKC';
COMMENT ON COLUMN "CREATOR"."SH_AKC_HIST".EVENT_DATE IS 'Дата и время события (с точностью до микросекунд)';
COMMENT ON COLUMN "CREATOR"."SH_AKC_HIST".PROD_ID IS 'ID записи SH_AKC, которая была изменена';
COMMENT ON COLUMN "CREATOR"."SH_AKC_HIST".OPERATION IS 'Тип операции: INSERT, UPDATE, DELETE';
COMMENT ON COLUMN "CREATOR"."SH_AKC_HIST".DB_USER IS 'Пользователь БД, выполнивший операцию';
COMMENT ON COLUMN "CREATOR"."SH_AKC_HIST".NEW_NAME IS 'Новое значение NAME';
