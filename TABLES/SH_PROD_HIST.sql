CREATE TABLE "CREATOR"."SH_PROD_HIST" (
    EVENT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE NOT NULL ENABLE,
    EVENT_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    PROD_ID NUMBER NOT NULL,
    OPERATION VARCHAR2(10) NOT NULL,
    DB_USER VARCHAR2(128) DEFAULT SYS_CONTEXT('USERENV', 'SESSION_USER') NOT NULL,
    -- Новые значения
    NEW_URL VARCHAR2(200),
    NEW_NAME VARCHAR2(300),
    NEW_SHOPID NUMBER,
    NEW_DEL NUMBER
);

COMMENT ON TABLE "CREATOR"."SH_PROD_HIST" IS 'История изменений таблицы SH_PROD';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".EVENT_DATE IS 'Дата и время события (с точностью до микросекунд)';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".PROD_ID IS 'ID записи SH_PROD, которая была изменена';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".OPERATION IS 'Тип операции: INSERT, UPDATE, DELETE';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".DB_USER IS 'Пользователь БД, выполнивший операцию';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".NEW_URL IS 'Новое значение URL';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".NEW_NAME IS 'Новое значение NAME';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".NEW_SHOPID IS 'Новое значение SHOPID';
COMMENT ON COLUMN "CREATOR"."SH_PROD_HIST".NEW_DEL IS 'Новое значение DEL';
